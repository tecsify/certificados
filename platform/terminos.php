                        <h1><strong>Política de Privacidad y Política de Datos de Tecsify</strong></h1>

                        <p><strong>Última actualización:</strong> 06 de mayo de 2023</p>

                        <p>En Tecsify, valoramos su privacidad y la protección de sus datos personales. Esta <strong>Política de Privacidad y Política de Datos</strong> tiene como objetivo informarle sobre cómo recopilamos, utilizamos, compartimos y protegemos sus datos personales. Al participar en nuestros eventos y proporcionarnos sus datos personales, usted acepta los términos y condiciones descritos en esta política.</p>

                        <h2><strong>1. Recopilación de Datos</strong></h2>

                        <p>Tecsify ha organizado múltiples eventos en los que participaron numerosas personas. Para generar certificados digitales de participación en estas conferencias y eventos, solicitamos de manera voluntaria a los usuarios que completen un formulario en el que brindan sus datos personales. Los datos recopilados incluyen, pero no se limitan a:</p>

                        <ul>
                            <li>Nombre</li>
                            <li>Apellido</li>
                            <li>Dirección de correo electrónico</li>
                            <li>Información de contacto</li>
                            <li>Datos de la conferencia a la que asistieron</li>
                        </ul>

                        <p>Estos datos son proporcionados voluntariamente por los usuarios a cambio de recibir sus certificados de participación en los eventos organizados por Tecsify.</p>

                        <h2><strong>2. Almacenamiento de Datos</strong></h2>

                        <p>Los datos proporcionados por los usuarios se almacenarán en la plataforma de Tecsify durante la existencia de los certificados. Los certificados de conferencias y charlas tienen una validez de 1 año desde su aprobación. Durante este período, los usuarios pueden acceder a sus certificados en cualquier momento utilizando el enlace proporcionado por Tecsify.</p>

                        <h2><strong>3. Derecho a la Eliminación</strong></h2>

                        <p>Si un usuario desea eliminar sus datos personales y certificados de participación de cualquier evento, puede hacerlo enviando una solicitud por correo electrónico a <a href="mailto:certificados@tecsify.com"><strong>certificados@tecsify.com</strong></a> desde la dirección de correo electrónico asociada a su cuenta. Tecsify procesará estas solicitudes de eliminación de datos en un plazo razonable.</p>

                        <h2><strong>4. Normas Internas de Certificación</strong></h2>

                        <p>Tecsify se reserva el derecho de borrar o deshabilitar cualquier certificado que no cumpla con nuestras normas internas. Estas normas incluyen, entre otros aspectos, la veracidad de la información proporcionada por el usuario y la conducta adecuada durante los eventos. La decisión de eliminar o deshabilitar un certificado se tomará de manera justa y objetiva.</p>

                        <h2><strong>5. Responsabilidad de los Usuarios</strong></h2>

                        <p>Tecsify no se responsabiliza de los datos proporcionados por los usuarios. Los usuarios son los únicos responsables de la exactitud y veracidad de la información proporcionada. Cualquier discordancia de datos o información incorrecta es completamente responsabilidad del usuario. Los usuarios deben proporcionar información precisa y actualizada.</p>

                        <h2><strong>6. Cambios en la Política</strong></h2>

                        <p>Tecsify se reserva el derecho de actualizar o modificar esta <strong>Política de Privacidad y Política de Datos</strong> en cualquier momento. Las actualizaciones se publicarán en nuestro sitio web y se notificarán a los usuarios según corresponda.</p>

                        <h2><strong>7. Contacto</strong></h2>

                        <p>Si tiene alguna pregunta o inquietud relacionada con esta política, o si desea ejercer sus derechos de privacidad, póngase en contacto con nosotros a través de <a href="mailto:contacto@tecsify.com"><strong>contacto@tecsify.com</strong></a>.</p>

                        <p>Esta <strong>Política de Privacidad y Política de Datos</strong> refleja nuestro compromiso con la privacidad y la protección de sus datos personales. Le recomendamos que la lea detenidamente y la revise periódicamente para mantenerse informado sobre nuestras prácticas de privacidad. Gracias por confiar en Tecsify.</p>

